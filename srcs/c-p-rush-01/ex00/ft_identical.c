int	ft_identical(int n, int r, int c, int **mat)
{
	int	i;

	i = 0;
	while (i < 4)
	{
		if (mat[r][i] == n)
			return (0);
		if (mat[i][c] == n)
			return (0);
		i += 1;
	}
	return (1);
}
